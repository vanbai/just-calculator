function sum(a, b) {
    return a + b;
}

function substract(a, b) {
    return a - b;
}

function multiply(a, b) {
    return a * b;
}

function divide(a, b) {
    return a / b;
}

function exponentiation(a, b) {
    return Math.pow(a, b);
}

function square(a, b = 0.5) {
    return Math.pow(a, b);
}
